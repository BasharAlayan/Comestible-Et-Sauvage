package com.example.bashar.comestibleetsauvage;

public class Plante
{
	public String nom;
	public String libelle;
	public String statut;
	public String location;
	public String image;

	public Plante(String nom, String libelle, String statut, String location, String image)
	{
		this.nom = nom;
		this.libelle = libelle;
		this.statut = statut;
		this.location = location;
		this.image = image;
	}
}
